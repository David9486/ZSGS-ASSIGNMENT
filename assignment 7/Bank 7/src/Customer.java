public class Customer {
    String custName;
    long accNumber;
    String dateOfBirth;
    String gender;
    String emailId;
    long phoneNumber;

   public Customer(String custName,long accNumber,String dateOfBirth,String gender,String emailId,long phoneNumber) {
       this.custName = custName;
       this.accNumber = accNumber;
       this.dateOfBirth = dateOfBirth;
       this.gender = gender;
       this.emailId = emailId;
       this.phoneNumber = phoneNumber;
   }
}
